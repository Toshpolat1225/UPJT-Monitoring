export type DictKey = keyof typeof dict;

const Ctx = createContext<{
  lang: Lang;
  t: (k: DictKey) => string;
  ln: (row: { name_uz?: string | null; name?: string | null } | null | undefined) => string;
}>({ lang: 'uz', t: (k) => k, ln: () => '' });

export function I18nProvider({ children }: { children: ReactNode }) {
  const t = (k: DictKey) => dict[k] ?? k;
  const ln = (row: { name_uz?: string | null; name?: string | null } | null | undefined) => row?.name_uz || row?.name || '?';
  return <Ctx.Provider value={{ lang: 'uz', t, ln }}>{children}</Ctx.Provider>;
}

export const useI18n = () => useContext(Ctx);

export function formatUnit(unit: string | null | undefined, _lang: Lang): string {
  if (!unit) return '';
  if (unit === 'm3') return 'm\u00b3';
  if (unit === 'litr') return 'l';
  return unit;
}
